class Node:
    def __init__(self, value):
        self.value = value
        self.next = None
        

class LinkedList:
    def __init__(self, value):
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node

        
    def append(self, value):
        new_node = Node(value)
        if self.head == None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        return True
  
def find_kth_from_end(self,k):
    temp1=self.head
    temp2=self.head
    for _ in range(k):
        if temp2 is None:
            return None
        temp2=temp2.next
    while temp2 is not None:
        temp1=temp1.next
        temp2=temp2.next
    return temp1
    
        




